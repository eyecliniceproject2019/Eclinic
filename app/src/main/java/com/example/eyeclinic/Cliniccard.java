package com.example.eyeclinic;

public class Cliniccard {
    private String date;
    private String pre;
    private String blood;
    private String sug;
    private String sphl;
    private String sphr;
    private String cyll;
    private String cylr;
    private String addl;
    private String addr;
    private String comment;
    private String initial;
    private String next;

    public Cliniccard() {
        this.date = date;
        this.pre = pre;
        this.blood = blood;
        this.sug = sug;
        this.sphl = sphl;
        this.sphr = sphr;
        this.cyll = cyll;
        this.cylr = cylr;
        this.addl = addl;
        this.addr = addr;
        this.comment = comment;
        this.initial = initial;
        this.next = next;
    }


    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPre() {
        return pre;
    }

    public void setPre(String pre) {
        this.pre = pre;
    }

    public String getBlood() {
        return blood;
    }

    public void setBlood(String blood) {
        this.blood = blood;
    }

    public String getSug() {
        return sug;
    }

    public void setSug(String sug) {
        this.sug = sug;
    }

    public String getSphl() {
        return sphl;
    }

    public void setSphl(String sphl) {
        this.sphl = sphl;
    }

    public String getSphr() {
        return sphr;
    }

    public void setSphr(String sphr) {
        this.sphr = sphr;
    }

    public String getCyll() {
        return cyll;
    }

    public void setCyll(String cyll) {
        this.cyll = cyll;
    }

    public String getCylr() {
        return cylr;
    }

    public void setCylr(String cylr) {
        this.cylr = cylr;
    }

    public String getAddl() {
        return addl;
    }

    public void setAddl(String addl) {
        this.addl = addl;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getInitial() {
        return initial;
    }

    public void setInitial(String initial) {
        this.initial = initial;
    }

    public String getNext() {
        return next;
    }

    public void setNext(String next) {
        this.next = next;
    }



}





